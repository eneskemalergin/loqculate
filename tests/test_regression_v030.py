# Test 1: LOD matches pinned reference values
# ---------------------------------------------------------------------------


class TestLODRegression:
    def test_lod_matches_pinned_values(self, all_cf_fits):
        """PiecewiseCF.lod() must match the pinned reference within 1e-8.

        The solver is deterministic; any deviation signals a regression in
        find_knot, _fit_and_constrain, or the LOD formula itself.
        """
        failures = []
        for pep, ref_lod in _PINNED_LOD.items():
            cf = all_cf_fits[pep]
            got = cf.lod()
            if np.isfinite(ref_lod):
                rel = abs(got - ref_lod) / max(abs(ref_lod), 1e-12)
                if rel >= 1e-8:
                    failures.append((pep, ref_lod, got, rel))
            else:
                # Both should be infinite
                if np.isfinite(got):
                    failures.append((pep, "inf", got, np.nan))
        assert not failures, (
            f"LOD regression failures in {len(failures)} peptide(s):\n"
            + "\n".join(f"  {p}: expected {e}, got {g} (rel={r:.2e})" for p, e, g, r in failures)
        )

    def test_same_partition_lod_agrees_with_wls(self, reference_data, all_cf_fits):
        """Same-partition peptides: PiecewiseCF and PiecewiseWLS LODs must agree
        within 1e-4.  Both methods fit the same data with the same constrained
        parameters when they select the same partition.
        """
        failures = []
        for pep in _SAME_PARTITION_PEPTIDES:
            mask = reference_data.peptide == pep
            x = reference_data.concentration[mask]
            y = reference_data.area[mask]
            wls = PiecewiseWLS(init_method="legacy", n_boot_reps=0, seed=42).fit(x, y)
            lod_wls = wls.lod()
            lod_cf = all_cf_fits[pep].lod()
            if np.isfinite(lod_wls) and np.isfinite(lod_cf):
                rel = abs(lod_cf - lod_wls) / max(abs(lod_wls), 1e-12)
                if rel >= 1e-4:
                    failures.append((pep, lod_wls, lod_cf, rel))
        assert not failures, (
            f"Same-partition LOD agreement failed for {len(failures)} peptide(s):\n"
            + "\n".join(f"  {p}: WLS={w:.6f}, CF={c:.6f} (rel={r:.2e})" for p, w, c, r in failures)
        )


