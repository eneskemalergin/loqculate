class TestCovariance:
    """Tests for PiecewiseCF.covariance().

    The method returns the 2x2 parameter covariance matrix for the linear
    segment: Cov = MSE_linear * _gram_inv where MSE uses ddof=2 (slope +
    intercept).  Returns None for unfitted models or slope=0 models.
    """

    def test_covariance_shape_and_type(self, reference_data):
        """covariance() must return a (2, 2) numpy array for a normally fitted
        peptide (finite LOD, slope > 0).
        """
        pep = "ADTGIAVEGATDAAR"
        mask = reference_data.peptide == pep
        x = reference_data.concentration[mask]
        y = reference_data.area[mask]
        cf = PiecewiseCF(n_boot_reps=0, seed=42).fit(x, y)
        assert cf.params_["slope"] > 0, "pre-condition: peptide must have slope > 0"

        cov = cf.covariance()
        assert cov is not None, "covariance() must not return None for a slope>0 peptide"
        assert isinstance(cov, np.ndarray)
        assert cov.shape == (2, 2)

    def test_covariance_diagonal_nonnegative(self, reference_data):
        """Diagonal entries of the covariance matrix are variances — they must
        be non-negative for any valid WLS fit.
        """
        pep = "ADTGIAVEGATDAAR"
        mask = reference_data.peptide == pep
        x = reference_data.concentration[mask]
        y = reference_data.area[mask]
        cf = PiecewiseCF(n_boot_reps=0, seed=42).fit(x, y)
        cov = cf.covariance()
        assert cov is not None
        assert cov[0, 0] >= 0, f"var(slope) = {cov[0, 0]} is negative"
        assert cov[1, 1] >= 0, f"var(intercept) = {cov[1, 1]} is negative"

    def test_covariance_is_symmetric(self, reference_data):
        """The covariance matrix must be symmetric to machine precision."""
        pep = "ADTGIAVEGATDAAR"
        mask = reference_data.peptide == pep
        x = reference_data.concentration[mask]
        y = reference_data.area[mask]
        cf = PiecewiseCF(n_boot_reps=0, seed=42).fit(x, y)
        cov = cf.covariance()
        assert cov is not None
        np.testing.assert_allclose(
            cov,
            cov.T,
            atol=1e-15,
            err_msg="covariance matrix is not symmetric",
        )

    def test_covariance_matches_lstsq_reference(self, reference_data):
        """The covariance matrix must agree with a reference computed directly
        via np.linalg.lstsq on the same linear segment, within rtol=1e-8.

        Reference formula:
            H = W^(1/2) * [x_lin | 1]    (design matrix, weighted by sqrt(W))
            beta, _, _, _ = lstsq(H, W^(1/2) * y_lin)
            residuals = y_lin - (a * x_lin + intercept)
            wrss = sum(W * residuals^2)
            mse = wrss / (n_lin - 2)
            cov_ref = mse * inv(H.T @ H)    # == mse * _gram_inv
        """
        pep = "VTAVVESPEGER"
        mask = reference_data.peptide == pep
        x = reference_data.concentration[mask]
        y = reference_data.area[mask]
        w = inverse_sqrt_weights(x)
        W = w**2

        cf = PiecewiseCF(n_boot_reps=0, seed=42).fit(x, y)
        assert cf.params_["slope"] > 0, "pre-condition: test peptide must have slope > 0"

        knot_x = cf.params_["knot_x"]
        lin_mask = x > knot_x
        x_lin = x[lin_mask]
        y_lin = y[lin_mask]
        W_lin = W[lin_mask]
        n_lin = int(np.sum(lin_mask))

        # Build weighted design matrix
        sqrt_W = np.sqrt(W_lin)
        H = np.column_stack([x_lin * sqrt_W, sqrt_W])  # [x | 1] * sqrt(W)
        beta, _, _, _ = np.linalg.lstsq(H, y_lin * sqrt_W, rcond=None)
        a_ref, b_ref = beta[0], beta[1]

        residuals = y_lin - (a_ref * x_lin + b_ref)
        wrss = float(np.sum(W_lin * residuals**2))
        mse = wrss / (n_lin - 2)

        G = H.T @ H  # = [[sum(W*x^2), sum(W*x)], [sum(W*x), sum(W)]]
        cov_ref = mse * np.linalg.inv(G)

        cov_cf = cf.covariance()
        assert cov_cf is not None
        np.testing.assert_allclose(
            cov_cf,
            cov_ref,
            rtol=1e-8,
            err_msg="covariance() does not match lstsq reference",
        )

    def test_covariance_none_when_not_fitted(self):
        """covariance() must return None (not raise) before fit() is called."""
        cf = PiecewiseCF(n_boot_reps=0, seed=42)
        assert cf.covariance() is None

    def test_covariance_none_when_slope_zero(self):
        """covariance() must return None when slope was clamped to 0.
        The linear segment was fit as a weighted mean (1-parameter); the
        2x2 Gram inverse is undefined for that model.
        """
        rng = np.random.default_rng(7)
        x_noise = np.repeat([0.1, 0.5, 1.0], 3)
        y_noise = 10 + rng.normal(0, 0.2, 9)
        x_lin = np.repeat([2.0, 5.0, 10.0], 3)
        y_lin = 12 - 0.5 * x_lin + rng.normal(0, 0.2, 9)  # decreasing → slope=0

        cf = PiecewiseCF(n_boot_reps=0, seed=42).fit(
            np.concatenate([x_noise, x_lin]),
            np.concatenate([y_noise, y_lin]),
        )
        assert cf.params_["slope"] == 0.0, "pre-condition: slope must be clamped to 0"
        assert cf.covariance() is None, (
            "covariance() must return None when slope=0; "
            "the 2x2 Gram inverse does not correspond to the fitted model"
        )


# ---------------------------------------------------------------------------
# Test 9: vectorized bootstrap (H6) — C5
# ---------------------------------------------------------------------------


class TestVectorizedBootstrap:
    """Vectorized bootstrap: correctness, determinism, and exact loop agreement.

    The vectorized path gates each candidate k on whether k is an INTERIOR
    point of unique(xb_i) for replicate i — matching find_knot(xb)[1:-1].
    This ensures knot selection is bit-identical to the loop path.  The only
    residual differences are sub-ULP FP accumulation in numpy's 2-D axis=1
    reductions vs the loop's 1-D scalar sums (max ~2e-7 absolute on the
    reference dataset, <1e-14 relative — machine-precision noise).

    Tests here verify:
    - Determinism (same seed → same output)
    - Different seeds → different output
    - Memory guard fallback emits ResourceWarning and returns valid summary
    - LOD regression is unaffected (LOD is analytical, no bootstrap)
    - All 26 finite-LOD reference peptides agree with the loop path to 1e-6
      in CV and 1e-3 relative in mean predictions (covers FP noise budget)
    - ADTGIAVEGATDAAR in particular agrees to 1e-10 (no FP noise for seed=42)
    """

    @pytest.fixture()
    def pep_data(self, reference_data):
        pep = "ADTGIAVEGATDAAR"
        mask = reference_data.peptide == pep
        x = reference_data.concentration[mask]
        y = reference_data.area[mask]
        w = inverse_sqrt_weights(x)
        W = w**2
        return x, y, W

    @pytest.fixture()
    def x_grid(self, pep_data):
        x, _, _ = pep_data
        return np.linspace(x.min(), x.max(), 200)

    # H6 — ADTGIAVEGATDAAR: shows 0 FP noise, so can hold to 1e-10
    def test_h6_cv_profile_agrees_with_loop(self, pep_data, x_grid):
        """ADTGIAVEGATDAAR seed=42 shows zero FP accumulation; CV must agree
        to better than 1e-10."""
        x, y, W = pep_data
        n_reps, seed = 100, 42

        _, summ_loop = _bootstrap_loop_cf(x, y, W, x_grid, n_reps, seed)
        _, summ_vec = _bootstrap_vectorized_cf(x, y, W, x_grid, n_reps, seed)

        cv_loop = summ_loop["cv"]
        cv_vec = summ_vec["cv"]

        finite = np.isfinite(cv_loop) & np.isfinite(cv_vec)
        assert finite.any(), "no finite CV values to compare"
        max_diff = float(np.max(np.abs(cv_loop[finite] - cv_vec[finite])))
        assert max_diff < 1e-10, (
            f"H6 FAIL for ADTGIAVEGATDAAR: max |cv_loop - cv_vec| = {max_diff:.3e}"
        )

    def test_h6_mean_pred_agrees_with_loop(self, pep_data, x_grid):
        """Mean predictions also agree to < 1e-10 for ADTGIAVEGATDAAR seed=42."""
        x, y, W = pep_data
        n_reps, seed = 100, 42

        _, summ_loop = _bootstrap_loop_cf(x, y, W, x_grid, n_reps, seed)
        _, summ_vec = _bootstrap_vectorized_cf(x, y, W, x_grid, n_reps, seed)

        max_diff = float(np.max(np.abs(summ_loop["mean"] - summ_vec["mean"])))
        assert max_diff < 1e-10, (
            f"H6 FAIL for ADTGIAVEGATDAAR: max |mean_loop - mean_vec| = {max_diff:.3e}"
        )

    def test_vectorized_is_deterministic(self, pep_data, x_grid):
        """Two calls with the same seed must produce bit-identical predictions."""
        x, y, W = pep_data
        pred1, _ = _bootstrap_vectorized_cf(x, y, W, x_grid, n_reps=50, seed=7)
        pred2, _ = _bootstrap_vectorized_cf(x, y, W, x_grid, n_reps=50, seed=7)
        np.testing.assert_array_equal(
            pred1, pred2, err_msg="vectorized bootstrap is not deterministic"
        )

    def test_vectorized_different_seeds_differ(self, pep_data, x_grid):
        """Different seeds must produce different replicate matrices."""
        x, y, W = pep_data
        pred1, _ = _bootstrap_vectorized_cf(x, y, W, x_grid, n_reps=50, seed=1)
        pred2, _ = _bootstrap_vectorized_cf(x, y, W, x_grid, n_reps=50, seed=2)
        assert not np.array_equal(pred1, pred2), "different seeds produced identical predictions"

    def test_memory_guard_fallback(self, pep_data, x_grid, monkeypatch):
        """When memory limit is tiny, a ResourceWarning must be emitted and
        the call must still return a valid (non-all-inf) summary via the loop
        fallback.
        """
        import loqculate.models.piecewise_cf as _mod

        monkeypatch.setattr(_mod, "VECTORIZED_BOOTSTRAP_MEMORY_LIMIT_MB", 0)

        with warnings.catch_warnings(record=True) as caught:
            warnings.simplefilter("always")
            x, y, W = pep_data
            _, summ = _bootstrap_vectorized_cf(x, y, W, x_grid, n_reps=20, seed=42)

        resource_warnings = [w for w in caught if issubclass(w.category, ResourceWarning)]
        assert resource_warnings, "expected a ResourceWarning from the memory guard"

        cv = summ["cv"]
        assert np.any(np.isfinite(cv)), "fallback summary must contain finite CV values"

    def test_lod_regression_unchanged(self, all_cf_fits):
        """Switching the default bootstrap path must not alter LOD values.

        LOD is computed analytically (no bootstrap involved), so all pinned
        values should be reproduced to within the original tolerance.
        """
        lod_values = {pep: fit.lod() for pep, fit in all_cf_fits.items()}
        for pep, expected in _PINNED_LOD.items():
            got = lod_values[pep]
            if expected is None:
                assert got is None, f"{pep}: expected LOD=None, got {got}"
            else:
                assert got is not None, f"{pep}: expected LOD={expected}, got None"
                if np.isinf(expected):
                    assert np.isinf(got), f"{pep}: expected inf LOD, got {got}"
                else:
                    assert abs(got - expected) < 1e-4, (
                        f"{pep}: LOD regression after C5: expected {expected}, got {got}"
                    )

    def test_h6_all_peptides_agree_with_loop(self, reference_data):
        """Vectorized path must agree with the loop path on ALL 26 finite-LOD
        reference peptides (n_reps=200, seed=42).

        Tolerance: max|cv_diff| < 1e-6 and max|mean_diff|/mean_pred < 1e-6.
        This comfortably covers the observed FP accumulation budget (~2e-7 abs,
        <1e-14 relative) while being tight enough to catch any knot-selection
        disagreement (which produces diffs of order 1e2–1e4 absolute).
        """
        n_reps, seed = 200, 42
        failures = []
        for pep in np.unique(reference_data.peptide):
            mask = reference_data.peptide == pep
            x = reference_data.concentration[mask]
            y = reference_data.area[mask]
            w = inverse_sqrt_weights(x)
            W = w**2

            cf = PiecewiseCF(n_boot_reps=0, seed=seed).fit(x, y)
            lod = cf.lod()
            if not np.isfinite(lod):
                continue

            x_grid = np.linspace(lod, float(np.max(x)), num=100)
            _, sl = _bootstrap_loop_cf(x, y, W, x_grid, n_reps, seed)
            _, sv = _bootstrap_vectorized_cf(x, y, W, x_grid, n_reps, seed)

            fin = np.isfinite(sl["cv"]) & np.isfinite(sv["cv"])
            d_cv = float(np.max(np.abs(sl["cv"][fin] - sv["cv"][fin]))) if fin.any() else 0.0

            mean_scale = float(np.nanmean(np.abs(sl["mean"])))
            d_mean_rel = float(np.max(np.abs(sl["mean"] - sv["mean"]))) / max(mean_scale, 1e-12)

            if d_cv >= 1e-6 or d_mean_rel >= 1e-6:
                failures.append((pep, d_cv, d_mean_rel))

        assert not failures, (
            f"Vectorized/loop disagreement on {len(failures)} peptide(s):\n"
            + "\n".join(
                f"  {p}: max|cv_diff|={dc:.3e}, max|mean_diff|/mean={dm:.3e}"
                for p, dc, dm in failures
            )
        )
